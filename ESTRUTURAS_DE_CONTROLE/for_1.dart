// For é indicado para quando tem uma quantidade certa de vezes que irá executar
main() {
  for (int a = 0; a <= 10; a++) {
    print("A = $a.");
  }
print("=============================");
  for (int a = 100; a >= 0; a -= 5) {
    print("A = $a");
  }
print("=============================");
  int b = 0;
  for (; b <= 10; b++) {
    print("B = $b");
  }
print("=============================");
  print("Fim!");
}
